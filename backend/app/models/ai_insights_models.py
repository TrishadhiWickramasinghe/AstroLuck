class InsightEngagementLog(Base):
    """Track how users engage with insights"""
    __tablename__ = "insight_engagement_logs"
    
    id = Column(String(36), primary_key=True, default=lambda: str(uuid.uuid4()))
    
    # Reference
    insight_id = Column(String(36), ForeignKey('daily_insights.id', ondelete='CASCADE'), nullable=True)
    insight = relationship("DailyInsight", back_populates="engagements")
    
    personalized_insight_id = Column(String(36), ForeignKey('daily_insights_personalized.id', ondelete='CASCADE'), nullable=True)
    personalized_insight = relationship("DailyInsightPersonalized", back_populates="engagements")
    
    user_id = Column(String(36), ForeignKey('user.id', ondelete='CASCADE'), nullable=False, index=True)
    user = relationship("User", back_populates="insight_engagements")
    
    # Engagement Events
    view_count = Column(Integer, default=0)
    opened_at = Column(DateTime, nullable=True)
    time_spent_seconds = Column(Integer, default=0)
    
    # Interactions
    clicked_to_details = Column(Boolean, default=False)
    clicked_lucky_times = Column(Boolean, default=False)
    clicked_lucky_numbers = Column(Boolean, default=False)
    shared_to_whatsapp = Column(Boolean, default=False)
    shared_to_social = Column(Boolean, default=False)
    
    # Save & Favorite
    is_saved = Column(Boolean, default=False)
    saved_at = Column(DateTime, nullable=True)
    
    is_bookmarked = Column(Boolean, default=False)
    bookmarked_at = Column(DateTime, nullable=True)
    
    # Push Notification
    notification_sent = Column(Boolean, default=False)
    notification_opened = Column(Boolean, default=False)
    opened_from_notification = Column(Boolean, default=False)
    
    # Source Tracking
    source = Column(String(50))  # daily_feed, notification, search, share_link
    entry_point = Column(String(50))  # home, settings, notification, etc
    
    created_at = Column(DateTime, default=datetime.utcnow, index=True)
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow)


