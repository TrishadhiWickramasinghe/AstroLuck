class InsightFeedback(Base):
    """User feedback on insights for AI training"""
    __tablename__ = "insight_feedback"
    
    id = Column(String(36), primary_key=True, default=lambda: str(uuid.uuid4()))
    
    user_id = Column(String(36), ForeignKey('user.id', ondelete='CASCADE'), index=True)
    user = relationship("User", back_populates="insight_feedback")
    
    insight_id = Column(String(36), ForeignKey('daily_insights.id', ondelete='CASCADE'), nullable=True)
    insight = relationship("DailyInsight", back_populates="feedback_list")
    
    personalized_insight_id = Column(String(36), ForeignKey('daily_insights_personalized.id', ondelete='CASCADE'), nullable=True)
    personalized_insight = relationship("DailyInsightPersonalized", back_populates="feedback")
    
    # Rating
    overall_rating = Column(Integer)  # 1-5 stars
    accuracy_rating = Column(Integer, nullable=True)  # How accurate was it (1-5)
    relevance_rating = Column(Integer, nullable=True)  # How relevant (1-5)
    
    # Text Feedback
    comment = Column(Text, nullable=True)
    
    # Structured Feedback
    was_accurate = Column(Boolean, nullable=True)
    was_helpful = Column(Boolean, nullable=True)
    would_recommend = Column(Boolean, nullable=True)
    
    feedback_tags = Column(JSON)  # ["too_generic", "helpful", "accurate", "inaccurate", ...]
    
    # Sentiment Analysis
    sentiment = Column(String(20))  # positive, neutral, negative
    emotion = Column(String(50))  # inspired, skeptical, validated, etc
    
    # For AI Training
    helpful_for_training = Column(Boolean, default=True)
    confidential = Column(Boolean, default=False)
    
    created_at = Column(DateTime, default=datetime.utcnow, index=True)
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow)


