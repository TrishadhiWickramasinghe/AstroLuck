"""AI Daily Insights Database Models - Personalized Astrological Intelligence"""

from sqlalchemy import Column, String, Integer, Float, DateTime, Boolean, Text, JSON, ForeignKey, Enum as SQLEnum
from sqlalchemy.orm import relationship
from datetime import datetime, timedelta
from app.db import Base
import enum
import uuid

# ============================================================================
# ENUMS
# ============================================================================

class InsightType(str, enum.Enum):
    """Types of daily insights"""
    GENERAL = "general"
    LUCKY_TIMES = "lucky_times"
    ROMANCE = "romance"
    CAREER = "career"
    HEALTH = "health"
    FINANCE = "finance"
    LUCKY_NUMBERS = "lucky_numbers"
    TAROT = "tarot"
    NUMEROLOGY = "numerology"


class InsightMood(str, enum.Enum):
    """Emotional tone of insight"""
    POSITIVE = "positive"
    NEUTRAL = "neutral"
    CAUTIOUS = "cautious"
    TRANSFORMATIVE = "transformative"


class InsightStatus(str, enum.Enum):
    """Insight generation status"""
    GENERATED = "generated"
    PERSONALIZED = "personalized"
    SCHEDULED = "scheduled"
    DELIVERED = "delivered"
    FAILED = "failed"


