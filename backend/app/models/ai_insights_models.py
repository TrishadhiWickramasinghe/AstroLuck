# 1. DAILY INSIGHT MODELS
# ============================================================================

class DailyInsight(Base):
    """Daily astrological insights generated by AI"""
    __tablename__ = "daily_insights"
    
    id = Column(String(36), primary_key=True, default=lambda: str(uuid.uuid4()))
    
    # Core Content
    date = Column(DateTime, default=datetime.utcnow, index=True)
    zodiac_sign = Column(String(20))  # aries, taurus, etc
    
    title = Column(String(200), nullable=False)
    short_summary = Column(String(500))  # Brief 1-2 line teaser
    full_content = Column(Text)  # Complete insight text
    
    # Metadata
    insight_type = Column(SQLEnum(InsightType), default=InsightType.GENERAL, index=True)
    mood = Column(SQLEnum(InsightMood), default=InsightMood.NEUTRAL)
    confidence_score = Column(Float, default=0.85)  # 0-1, how confident in prediction
    
    # Astrological Data
    moon_phase = Column(String(50))  # new_moon, waxing_crescent, etc
    current_planetary_positions = Column(JSON)  # {planet: sign, ...}
    mercury_retrograde = Column(Boolean, default=False)
    venus_aspects = Column(String(200))  # Specific Venus influence
    mars_aspects = Column(String(200))  # Specific Mars influence
    
    # Content Sections (JSON structured data)
    sections = Column(JSON)  # {
    #   "lucky_times": {"start": "09:00", "end": "12:00"},
    #   "lucky_colors": ["red", "gold"],
    #   "lucky_numbers": [7, 14, 21],
    #   "element_focus": "fire",
    #   "power_affirmation": "I am...",
    #   "tarot_card": "The Magician",
    #   "numerology": {"life_path": 5, "message": "..."}
    # }
    
    # AI Generation Info
    ai_model_version = Column(String(50))  # gpt-4, claude-3, etc
    generation_prompt_hash = Column(String(64))  # Track prompt uniqueness
    generation_method = Column(String(50))  # algorithmic, llm, hybrid
    
    # Performance Tracking
    view_count = Column(Integer, default=0)
    interaction_count = Column(Integer, default=0)  # clicks, shares
    save_count = Column(Integer, default=0)
    
    # Relationships
    user_id = Column(String(36), ForeignKey('user.id', ondelete='CASCADE'), nullable=True)  # If personalized for user
    user = relationship("User", back_populates="daily_insights")
    
    engagements = relationship("InsightEngagementLog", back_populates="insight", cascade="all, delete-orphan")
    feedback_list = relationship("InsightFeedback", back_populates="insight", cascade="all, delete-orphan")
    
    # Timestamps
    created_at = Column(DateTime, default=datetime.utcnow, index=True)
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow)
    published_at = Column(DateTime, nullable=True)
    
    status = Column(SQLEnum(InsightStatus), default=InsightStatus.GENERATED, index=True)


class DailyInsightPersonalized(Base):
    """Personalized version of daily insight for specific user"""
    __tablename__ = "daily_insights_personalized"
    
    id = Column(String(36), primary_key=True, default=lambda: str(uuid.uuid4()))
    
    # Reference to base insight
    base_insight_id = Column(String(36), ForeignKey('daily_insights.id', ondelete='CASCADE'), nullable=False)
    base_insight = relationship("DailyInsight")
    
    user_id = Column(String(36), ForeignKey('user.id', ondelete='CASCADE'), nullable=False, index=True)
    user = relationship("User", back_populates="personalized_insights")
    
    # Personalization Factors
    personalized_title = Column(String(200))
    personalized_content = Column(Text)  # Customized with user's name, history, etc
    
    personalization_factors = Column(JSON)  # {
    #   "used_birth_time": true,
    #   "considered_transit_date": true,
    #   "incorporated_user_preferences": ["career", "romance"],
    #   "personalization_level": 0.9
    # }
    
    # User-Specific Sections
    personalized_sections = Column(JSON)  # Enhanced sections with user data
    user_history_context = Column(String(500))  # Reference to user's past insights
    
    # Delivery Tracking
    scheduled_delivery_time = Column(DateTime, nullable=True, index=True)
    delivered_at = Column(DateTime, nullable=True)
    opened_at = Column(DateTime, nullable=True)
    
    # Engagement
    engagement_count = Column(Integer, default=0)
    time_spent_seconds = Column(Integer, default=0)
    
    # Relationships
    engagements = relationship("InsightEngagementLog", foreign_keys="InsightEngagementLog.personalized_insight_id", 
                              back_populates="personalized_insight", cascade="all, delete-orphan")
    feedback = relationship("InsightFeedback", foreign_keys="InsightFeedback.personalized_insight_id",
                           back_populates="personalized_insight")
    
    created_at = Column(DateTime, default=datetime.utcnow)
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow)


