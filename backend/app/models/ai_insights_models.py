class UserInsightPreference(Base):
    """User preferences for daily insights"""
    __tablename__ = "user_insight_preferences"
    
    id = Column(String(36), primary_key=True, default=lambda: str(uuid.uuid4()))
    user_id = Column(String(36), ForeignKey('user.id', ondelete='CASCADE'), unique=True, index=True)
    user = relationship("User", back_populates="insight_preferences", uselist=False)
    
    # Delivery Preferences
    insights_enabled = Column(Boolean, default=True)
    delivery_time = Column(String(5))  # HH:MM format, e.g., "08:00"
    timezone = Column(String(50), default="UTC")
    
    # Frequency
    frequency = Column(String(20), default="daily")  # daily, weekly, custom
    
    # Channel Preferences
    send_via_email = Column(Boolean, default=True)
    send_via_push = Column(Boolean, default=True)
    send_via_whatsapp = Column(Boolean, default=False)
    
    # Content Preferences
    preferred_insight_types = Column(JSON)  # ["general", "lucky_times", "career", ...]
    preferred_mood = Column(String(50), default="positive")  # positive, mixed, all
    
    # Personalization Level
    personalization_level = Column(String(20), default="high")  # low, medium, high
    include_birth_chart_insights = Column(Boolean, default=True)
    include_daily_transit_data = Column(Boolean, default=True)
    include_numerology = Column(Boolean, default=False)
    include_tarot = Column(Boolean, default=False)
    
    # Language & Style
    language = Column(String(10), default="en")
    content_length = Column(String(20), default="medium")  # short, medium, long
    writing_style = Column(String(50), default="inspiring")  # inspiring, scientific, mystical
    
    # Zodiac Sign Tracking
    tracked_zodiac_signs = Column(JSON)  # [self, partner, friend, ...]
    
    # Quiet Hours
    quiet_hours_enabled = Column(Boolean, default=True)
    quiet_hours_start = Column(String(5))  # HH:MM, e.g., "22:00"
    quiet_hours_end = Column(String(5))  # HH:MM, e.g., "07:00"
    
    # Engagement Settings
    auto_save_favorites = Column(Boolean, default=False)
    enable_notifications = Column(Boolean, default=True)
    enable_weekly_digest = Column(Boolean, default=True)
    
    created_at = Column(DateTime, default=datetime.utcnow)
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow)


class InsightHistory(Base):
    """Historical record of all insights viewed by user"""
    __tablename__ = "insight_history"
    
    id = Column(String(36), primary_key=True, default=lambda: str(uuid.uuid4()))
    
    user_id = Column(String(36), ForeignKey('user.id', ondelete='CASCADE'), index=True)
    user = relationship("User", back_populates="insight_history")
    
    insight_id = Column(String(36), ForeignKey('daily_insights.id', ondelete='SET NULL'), nullable=True)
    insight = relationship("DailyInsight")
    
    personalized_insight_id = Column(String(36), ForeignKey('daily_insights_personalized.id', ondelete='CASCADE'), nullable=True)
    
    # Content Summary
    title = Column(String(200))
    date = Column(DateTime, index=True)
    zodiac_sign = Column(String(20))
    insight_type = Column(SQLEnum(InsightType))
    
    # Interaction Summary
    viewed = Column(Boolean, default=True)
    viewed_at = Column(DateTime, nullable=True)
    time_spent_seconds = Column(Integer, default=0)
    
    favorites = Column(Boolean, default=False)
    shared = Column(Boolean, default=False)
    rating = Column(Integer, nullable=True)  # 1-5 stars
    
    # Archival
    archived = Column(Boolean, default=False)
    archived_at = Column(DateTime, nullable=True)
    
    created_at = Column(DateTime, default=datetime.utcnow, index=True)
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow)


